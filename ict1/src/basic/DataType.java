package basic;

public class DataType {
	public static void main(String[] args) {

		// 정해져있는 data type
		byte b = 127;
		short s = 12312;
		int i = 123123;
		long l = 12343534534534l;

		float f = 1.23f;
		double d = 1.23;

		char a = 'a';

		boolean b1 = true;
		b1 = false;
		
		boolean bl = 1==1;
		
		//비교연산자 -->조건문(if문), 반복문사용
		int x = 2;
		int z = 4;
		
		System.out.println(x==z);
		System.out.println(x!=z);
		System.out.println(x>z);
		System.out.println(x<z);
		System.out.println(x>=z);
		System.out.println(x<=z);
		
		
		
		// 정해져있지 않는 data type
		String str = "이정혜";
		
		

		
	}

}
